	INSERT INTO AUTOMATION_API_QUERY(QUERY_ID, QUERY_KEY, QUERY_DESC, QUERY_TEXT, ACTIVE_FLAG, VERIFIED_BY, VERIFIED_DATE, CREATE_DATETIME, CREATE_USER_ID, MODIFY_DATETIME, MODIFY_USER_ID,CATEGORY) VALUES(NEXTVAL('AUTOMATION_QUERY_ID_SEQ'), :queryKey, :queryDesc, :queryText, :activeFlag, :verifiedBy, :verifiedDate, CURRENT_TIMESTAMP, :createUserId, null, null,:category)
}

CMDACTION_UPDATE_QUICK_ACTIONS {
	UPDATE AUTOMATION_API_QUERY SET QUERY_ID = :queryId, QUERY_KEY = :queryKey, QUERY_DESC = :queryDesc, QUERY_TEXT = :queryText, ACTIVE_FLAG = :activeFlag, VERIFIED_BY = :verifiedBy, VERIFIED_DATE = :verifiedDate, MODIFY_DATETIME = CURRENT_TIMESTAMP, MODIFY_USER_ID = :modifyUserId,CATEGORY=:category WHERE QUERY_ID = :queryId
}

CMDACTION_DELETE_QUICK_ACTIONS {
	DELETE FROM AUTOMATION_API_QUERY WHERE QUERY_ID = :queryId
}

