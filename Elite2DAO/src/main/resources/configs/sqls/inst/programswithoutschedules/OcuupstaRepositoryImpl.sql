
OCUUPSTA_FIND_RGPSPRGSTAT {
 	SELECT   REF_CODE.DESCRIPTION , REF_CODE.CODE     FROM REFERENCE_CODES REF_CODE    WHERE (:PLOVDOMAIN= 'PS_PRG_STAT'
 	AND DOMAIN = 'PS_PRG_STAT'           AND (ACTIVE_FLAG = 'Y' OR 'ENTER-QUERY' = 'ENTER-QUERY' )    
 	AND (   (:PCURRENTSTATUS = 'REF' AND REF_CODE.CODE IN('REM' , 'SUSP' , 'ABA' , '121' ) ) 
 	OR (:PCURRENTSTATUS = 'ALLOC' AND REF_CODE.CODE IN('SUSP' , 'ABA' , 'COMP' , 'REF' , '121' ) ) 
 	OR (:PCURRENTSTATUS = 'SUSP' AND REF_CODE.CODE IN('ALLOC' , 'ABA' , '121' ) )         
 	OR (:PCURRENTSTATUS = '121' AND REF_CODE.CODE IN('ALLOC' , 'COMP' , 'SUSP' , 'ABA' ) )  
 	OR (:PCURRENTSTATUS = 'ABA' AND REF_CODE.CODE IN('SUSP' ) )              
 	OR (:PCURRENTSTATUS = 'REM' AND REF_CODE.CODE IN('REF' ) )           
 	OR (:PCURRENTSTATUS = 'COMP' AND REF_CODE.CODE IN('ALLOC' , '121' ) ) ) )
 	OR (:PLOVDOMAIN= 'OPO_WR_STAT'           AND DOMAIN = 'OPO_WR_STAT' 
 	AND (ACTIVE_FLAG = 'Y' OR 'ENTER-QUERY' = 'ENTER-QUERY' )  AND ((:PCURRENTSTATUS = 'REF' AND REF_CODE.CODE IN('ACCEP' , 'REJ' ) )
 	OR (:PCURRENTSTATUS = 'ACCEP' AND REF_CODE.CODE IN('END' , 'REF' , 'SUSP' ) ) ) ) ORDER BY REF_CODE.LIST_SEQ , REF_CODE.DESCRIPTION , REF_CODE.CODE
}

OCUUPSTA_FIND_RGPSPRGOBSTS {
 	SELECT REF_CODE.DESCRIPTION  ,REF_CODE.CODE FROM   REFERENCE_CODES REF_CODE WHERE   DOMAIN = 'PS_PRG_OBSTS' AND 
 	ACTIVE_FLAG = 'Y'   AND PARENT_CODE = :PARENTCODE ORDER BY LIST_SEQ , DESCRIPTION ,CODE
}

OCUUPSTA_OFFPRGOBLHTY_FIND_OFFENDER_PRG_OBLIGATION_HTY {
 	SELECT * FROM OFFENDER_PRG_OBLIGATION_HTY WHERE OFFENDER_PRG_OBLIGATION_ID=:OFFENDER_PRG_OBLIGATION_ID ORDER BY STATUS_CHANGE_DATE DESC
}

