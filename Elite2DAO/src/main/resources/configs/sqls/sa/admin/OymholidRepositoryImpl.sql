OYMHOLID_CSLDGHC_DELETE_CASELOAD_GRP_HOL_COMPENS { 
	DELETE FROM CASELOAD_GRP_HOL_COMPENS where HOLIDAY_EVENT_ID = :holidayEventId AND WORK_GROUP_ID = :workGroupId
}


