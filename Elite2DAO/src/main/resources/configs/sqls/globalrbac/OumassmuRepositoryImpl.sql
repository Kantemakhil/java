OUMASSMU_MODPRIV_FIND_MODULE_PRIVILEGES {
 	SELECT MP. MODULE_NAME , MP.ROLE_ID , MP.ACCESS_PRIVILEGE , MP.VERIFICATION_FLAG , MP.CREATE_DATETIME , MP.CREATE_USER_ID , MP.MODIFY_DATETIME , MP.MODIFY_USER_ID , MP.SEAL_FLAG, mod.description, mod.module_type 
	FROM MODULE_PRIVILEGES MP JOIN OMS_MODULES MOD ON  MOD.MODULE_NAME = MP.MODULE_NAME
	WHERE  MP.ROLE_ID = :ROLE_ID AND    MOD.MODULE_TYPE IN ('SCREEN','MENU','REPORT','TAB') ORDER BY MP.MODULE_NAME ASC
}
OUMASSMU_MODPRIV_INSERT_MODULE_PRIVILEGES {
insert into MODULE_PRIVILEGES(MODULE_NAME , ROLE_ID , ACCESS_PRIVILEGE , VERIFICATION_FLAG , CREATE_DATETIME , CREATE_USER_ID , MODIFY_DATETIME , SEAL_FLAG ) values(:moduleName , :roleId , :accessPrivilege , :verificationFlag , CURRENT_TIMESTAMP , :createUserId , null , :sealFlag )
}

OUMASSMU_MODPRIV_UPDATE_MODULE_PRIVILEGES {
   update MODULE_PRIVILEGES set MODULE_NAME = :moduleName , ROLE_ID = :roleId , ACCESS_PRIVILEGE = :accessPrivilege , VERIFICATION_FLAG = :verificationFlag , MODIFY_DATETIME = CURRENT_TIMESTAMP , MODIFY_USER_ID =:modifyUserId , SEAL_FLAG = :sealFlag where MODULE_NAME = :moduleName and ROLE_ID = :roleId 
}

OUMASSMU_MODPRIV_DELETE_MODULE_PRIVILEGES { 
	DELETE FROM MODULE_PRIVILEGES where MODULE_NAME  = :moduleName AND ROLE_ID  = :roleId
}


