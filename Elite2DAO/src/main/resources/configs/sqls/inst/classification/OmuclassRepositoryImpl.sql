
OMUCLASS_SUBTOTALS_FIND_ASSESSMENT_SECTION_SCORES_V1 {
 select OFFENDER_BOOK_ID , ASSESSMENT_SEQ , PARENT_ASSESSMENT_ID , EFFECTIVE_DATE , ASSESSMENT_ID , section , DESCRIPTION , SCORE from ASSESSMENT_SECTION_SCORES_V1 where ASSESSMENT_SEQ = :ASSESSMENT_SEQ and PARENT_ASSESSMENT_ID = :PARENT_ASSESSMENT_ID and OFFENDER_BOOK_ID = :OFFENDER_BOOK_ID
}

OMUCLASS_PRE_QUERY_GET_CASELOADTYPE {
	SELECT CASELOAD_TYPE   FROM CASELOADS   WHERE CASELOAD_ID =   (  SELECT WORKING_CASELOAD_ID      FROM STAFF_MEMBERS        WHERE USER_ID = :userName)
}
OMUCLASS_PRE_QUERY_GET_SECURITYLEVEL {
	SELECT  DESCRIPTION FROM  REFERENCE_CODES   WHERE  DOMAIN = :DOMAIN AND  CODE IN( SELECT  SUPERVISION_LEVEL_TYPE FROM  ASSESSMENT_SUPERVISIONS
	   WHERE  ASSESSMENT_ID = :ASSESSMENT_ID AND  :SCORE >=  MINI_SCORE  AND  :SCORE <=  MAX_SCORE)
}
