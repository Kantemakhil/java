OUMASSMU_FIND_CGFKMODPRIVACCESSPRIVILEGE {
 	SELECT REF_CODE.CODE  CODE   ,REF_CODE.DESCRIPTION  DESCRIPTION        ,REF_CODE.LIST_SEQ  LIST_SEQ FROM   REFERENCE_CODES REF_CODE WHERE   DOMAIN = 'ACCESS_PRIVI' AND ACTIVE_FLAG = 'Y' AND EXPIRED_DATE IS NULL ORDER BY  REF_CODE.LIST_SEQ        ,REF_CODE.DESCRIPTION
}

OUMASSMU_FIND_CGFKMODPRIVMODULENAME {
 	SELECT OMS_MOD.MODULE_NAME MODULE_NAME  ,OMS_MOD.DESCRIPTION DESCRIPTION  ,OMS_MOD.MODULE_TYPE MODULE_TYPE FROM OMS_MODULES OMS_MOD WHERE MODULE_TYPE IN ('SCREEN' ,'MENU' ,'REPORT' , 'TAB' ) ORDER BY OMS_MOD.MODULE_NAME  ,OMS_MOD.MODULE_TYPE
}

