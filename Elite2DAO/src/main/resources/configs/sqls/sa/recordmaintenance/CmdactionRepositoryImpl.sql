CMDACTION_QUICK_ACTIONS_EXECUTE_QUERY {
	SELECT QUERY_ID, QUERY_KEY, QUERY_DESC, CATEGORY,QUERY_TEXT, ACTIVE_FLAG, VERIFIED_BY, VERIFIED_DATE, CREATE_DATETIME, CREATE_USER_ID, MODIFY_DATETIME, MODIFY_USER_ID FROM AUTOMATION_API_QUERY ORDER BY MODIFY_DATETIME DESC
}

