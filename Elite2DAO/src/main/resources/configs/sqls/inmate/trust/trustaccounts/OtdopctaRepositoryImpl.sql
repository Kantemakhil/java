
OTDOPCTA_OFFTA_FIND_OFFENDER_TRUST_ACCOUNTS {
 	SELECT CASELOAD_ID ,OFFENDER_ID ,ACCOUNT_CLOSED_FLAG ,HOLD_BALANCE ,CURRENT_BALANCE ,MODIFY_DATE ,MODIFY_USER_ID ,LIST_SEQ ,NOTIFY_DATE ,CREATE_DATETIME ,CREATE_USER_ID ,MODIFY_DATETIME ,SEAL_FLAG   FROM OFFENDER_TRUST_ACCOUNTS  /* where  */
}
OTDOPCTA_OFFTA_UPDATE_OFFENDER_TRUST_ACCOUNTS {
	UPDATE OFFENDER_TRUST_ACCOUNTS set ACCOUNT_CLOSED_FLAG  = :accountClosedFlag ,HOLD_BALANCE  = :holdBalance ,CURRENT_BALANCE  = :currentBalance ,MODIFY_DATE  = :modifyDate ,MODIFY_USER_ID  = :modifyUserId ,LIST_SEQ  = :listSeq ,NOTIFY_DATE  = :notifyDate ,CREATE_DATETIME  = :createDatetime ,CREATE_USER_ID  = :createUserId ,MODIFY_DATETIME  = :modifyDatetime ,SEAL_FLAG  = :sealFlag WHERE CASELOAD_ID  = :caseloadId and OFFENDER_ID  = :offenderId
}

OTDOPCTA_OFFTA_DELETE_OFFENDER_TRUST_ACCOUNTS { 
	DELETE FROM OFFENDER_TRUST_ACCOUNTS  where CASELOAD_ID  = :caseloadId  and OFFENDER_ID  = :offenderId
}

