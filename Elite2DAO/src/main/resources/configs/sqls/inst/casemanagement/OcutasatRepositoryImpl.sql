
OCUTASAT_FIND_RGAREATYPE {
  SELECT RC.DESCRIPTION , RC.CODE FROM REFERENCE_CODES RC WHERE DOMAIN = 'AREA_TYPE' AND CODE IN ('COMM' ,'INST' ) AND ACTIVE_FLAG = 'Y' ORDER BY LIST_SEQ ,DESCRIPTION ,CODE 
}

OCUTASAT_FIND_RGAREA {
  SELECT A.DESCRIPTION , A.AREA_CODE CODE FROM AREAS A WHERE A.AREA_TYPE = :NBTAREATYPE AND A.AREA_CLASS NOT IN ('REGION' , 'SUB_AREA' ) AND ACTIVE_FLAG = 'Y' ORDER BY LIST_SEQ ,DESCRIPTION ,AREA_CODE
}

OCUTASAT_TEAMS_FIND_TEAMS {
 	SELECT TEAM_CODE ,DESCRIPTION ,ACTIVE_FLAG ,EXPIRY_DATE ,CREATE_USER_ID ,MODIFY_USER_ID ,MODIFY_DATETIME ,CREATE_DATETIME ,TEAM_ID   FROM automation_teams  
}
