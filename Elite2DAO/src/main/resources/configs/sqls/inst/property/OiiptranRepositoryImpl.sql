OIIPTRAN_ITMTX_FIND_OFFENDER_PPTY_ITEM_TXNS {
 	select
	PROPERTY_ITEM_TXN_ID,
	OFFENDER_BOOK_ID,
	PROPERTY_ITEM_SEQ,
	EVENT_SEQ,
	FROM_STATUS_CODE,
	TO_STATUS_CODE,
	CREATE_DATE,
	CREATE_USER_ID,
	COMMENT_TEXT,
	AGY_LOC_ID,
	DISPOSED_TO_OFFENDER_FLAG,
	VERIFICATION_FLAG,
	VERIFY_FLAG,
	DISPOSED_TO_CORP_ID,
	DISPOSED_TO_PERSON,
	PROPERTY_CONTAINER_ID,
	PROPERTY_CONTAINER_TXN_ID,
	TO_PROPERTY_CONTAINER_ID,
	DISPOSED_TO_PERSON_ID,
	COLOR,
	MAKE,
	SERIAL_NO,
	CREATE_DATETIME,
	MODIFY_DATETIME,
	MODIFY_USER_ID,
	SEAL_FLAG,
	CORP.description as DISPOSED_TO_CORP_NAME,
	ACTION_CODE,
	ACTION_REASON
from
	OFFENDER_PPTY_ITEM_TXNS OFF_PITO
left outer join (
	select
		TO_CHAR(v.CORPORATE_ID) code,
		v.CORPORATE_NAME description
	from
		CORPORATE_TYPES CT
	inner join CORPORATES v on
		v.corporate_id = CT.corporate_id
	where
		ct.corporate_type = 'DISP')CORP on
	OFF_PITO.DISPOSED_TO_CORP_ID::text = CORP.CODE
}

