OIIPTRAN_AGY_LOCATION_LIST {
SELECT C.DESCRIPTION FROM INT_LOC_USAGE_LOCATIONS A ,        INTERNAL_LOCATION_USAGES B ,        AGENCY_INTERNAL_LOCATIONS C
WHERE A.INTERNAL_LOCATION_USAGE_ID = B.INTERNAL_LOCATION_USAGE_ID    AND B.INTERNAL_LOCATION_USAGE = 'PROP'
 AND B.AGY_LOC_ID IN (SELECT CAL.AGY_LOC_ID  FROM CASELOAD_AGENCY_LOCATIONS CAL WHERE CAL.CASELOAD_ID = :caseLoadId
 AND CAL.AGY_LOC_ID NOT IN ( 'OUT' , 'TRN'  ) )     AND A.INTERNAL_LOCATION_ID = C.INTERNAL_LOCATION_ID
AND ((C.ACTIVE_FLAG = 'Y'    AND C.DEACTIVATE_DATE IS NULL )
 OR ('' = 'ENTER-QUERY' ) )   ORDER BY  C.AGY_LOC_ID ,A.LIST_SEQ  ,C.DESCRIPTION
 }
