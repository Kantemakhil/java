
OUMWMENU_FIND_RGMENUSNAME {
 	SELECT CODE  , DESCRIPTION    FROM REFERENCE_CODES   WHERE DOMAIN = 'WORKFLO_MENU'  ORDER BY LIST_SEQ  ,CODE
}

OUMWMENU_FIND_RGSCREENSMODULENAME {
 	SELECT OMS_MOD.MODULE_NAME  CODE ,OMS_MOD.DESCRIPTION  DESCRIPTION FROM   OMS_MODULES OMS_MOD WHERE  OMS_MOD.MODULE_TYPE = 'SCREEN'  ORDER BY MODULE_NAME
}

OUMWMENU_FIND_RGCASELOADTYPE {

SELECT
    ref_code.code code,
    ref_code.description description
FROM
    reference_codes ref_code
WHERE
        domain  IN ('CLOAD_TYPE') AND CODE NOT IN ('AUTO','BOTH')
    AND (
        active_flag = 'Y'
    )
ORDER BY list_seq,code


}

OUMWMENU_WFFOLDERS_FIND_WORKFLOW_FOLDERS {
 	
 	SELECT  WORKFLOW_CODE,DESCRIPTION,WORKFLOW_SEQ,MODIFY_USER_ID,MODIFY_DATETIME,CASELOAD_TYPE,CREATE_DATETIME,CREATE_USER_ID,SEAL_FLAG ,ACTIVE_FLAG ,EXPIRY_DATE FROM WORKFLOW_FOLDERS
}
OUMWMENU_WFFOLDERS_INSERT_WORKFLOW_FOLDERS {
 insert into WORKFLOW_FOLDERS(WORKFLOW_CODE, DESCRIPTION, WORKFLOW_SEQ, MODIFY_DATETIME, CASELOAD_TYPE, CREATE_DATETIME, CREATE_USER_ID, SEAL_FLAG,ACTIVE_FLAG,EXPIRY_DATE, modify_user_id) values(:workFlowCode, :description, :workFlowSeq, NULL, :caseLoadType, CURRENT_TIMESTAMP, :createUserId, :sealFlag,:activeFlag,:expiryDate,NULL) 	
}

OUMWMENU_WFFOLDERS_UPDATE_WORKFLOW_FOLDERS {
 update WORKFLOW_FOLDERS set WORKFLOW_CODE = :workFlowCode, DESCRIPTION = :description, WORKFLOW_SEQ = :workFlowSeq, MODIFY_USER_ID = :modifyUserId, MODIFY_DATETIME = CURRENT_TIMESTAMP, CASELOAD_TYPE = :caseLoadType, SEAL_FLAG = :sealFlag ,ACTIVE_FLAG = :activeFlag , EXPIRY_DATE = :expiryDate where WORKFLOW_CODE = :workFlowCode 	

}

OUMWMENU_WFFOLDERS_DELETE_WORKFLOW_FOLDERS { 
	DELETE FROM WORKFLOW_FOLDERS  where  WORKFLOW_CODE = :workFlowCode 
	
}
OUMWMENU_WFFOLDERS_DELETE_WORKFLOW_FOLDERS_PRE_DELETE { 
DELETE FROM WORKFLOW_SCREENS W
   WHERE W.WORKFLOW_CODE = :workFlowCode
	
}
OUMWMENU_WFSCREENS_FIND_WORKFLOW_SCREENS {
 	SELECT WORKFLOW_CODE,MODULE_NAME,DESCRIPTION,WORKFLOW_SEQ,MODIFY_USER_ID,MODIFY_DATETIME,CASELOAD_TYPE,CREATE_DATETIME,CREATE_USER_ID,SEAL_FLAG,TOOL_TIP FROM WORKFLOW_SCREENS
 	WHERE WORKFLOW_CODE  = :WORKFLOW_CODE 
 	ORDER BY WORKFLOW_SEQ 
}
OUMWMENU_WFSCREENS_INSERT_WORKFLOW_SCREENS {
 insert into WORKFLOW_SCREENS(WORKFLOW_CODE, MODULE_NAME, DESCRIPTION, WORKFLOW_SEQ, MODIFY_DATETIME, CASELOAD_TYPE, CREATE_DATETIME, CREATE_USER_ID, SEAL_FLAG,modify_user_id,TOOL_TIP) values(:workFlowCode, :moduleName, :description, :workFlowSeq, NULL, :caseLoadType, CURRENT_TIMESTAMP, :createUserId, :sealFlag,NULL,:toolTip) 
} 

OUMWMENU_WFSCREENS_UPDATE_WORKFLOW_SCREENS {
 update WORKFLOW_SCREENS set WORKFLOW_CODE = :workFlowCode, MODULE_NAME = :moduleName, DESCRIPTION = :description, WORKFLOW_SEQ = :workFlowSeq, MODIFY_USER_ID = :modifyUserId, MODIFY_DATETIME = CURRENT_TIMESTAMP, CASELOAD_TYPE = :caseLoadType, SEAL_FLAG = :sealFlag, TOOL_TIP = :toolTip where WORKFLOW_CODE = :workFlowCode and MODULE_NAME = :moduleName 
}

OUMWMENU_WFSCREENS_DELETE_WORKFLOW_SCREENS { 
	DELETE FROM WORKFLOW_SCREENS where  
	 WORKFLOW_CODE = :workFlowCode  and MODULE_NAME  = :moduleName 
	
}


