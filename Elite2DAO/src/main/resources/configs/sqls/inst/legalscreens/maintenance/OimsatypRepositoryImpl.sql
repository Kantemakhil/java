
OIMSATYP_FIND_CGFKSENTENCEADJUSTMENTSDSP {
 	SELECT REF_CODE1.DESCRIPTION  DSP_DESCRIPTION        ,REF_CODE1.DOMAIN  DSP_DOMAIN        ,REF_CODE1.LIST_SEQ  DSP_LIST_SEQ        ,REF_CODE1.CODE  DEBIT_CREDIT_CODE FROM   REFERENCE_CODES REF_CODE1 WHERE   REF_CODE1.DOMAIN = 'AC_TXN_POST' AND EXPIRED_DATE IS NULL AND ACTIVE_FLAG = 'Y' ORDER BY  REF_CODE1.LIST_SEQ ASC
}

OIMSATYP_FIND_CGFKSENTENCEADJUSTMENTSUSAG {
 	SELECT REF_CODE.CODE  CODE          ,REF_CODE.DOMAIN  DSP_DOMAIN2        ,REF_CODE.DESCRIPTION  DESCRIPTION        ,REF_CODE.LIST_SEQ  DSP_LIST_SEQ2 FROM   REFERENCE_CODES REF_CODE WHERE   REF_CODE.DOMAIN = 'USG_CODE' AND EXPIRED_DATE IS NULL AND ACTIVE_FLAG = 'Y' ORDER BY DSP_LIST_SEQ2
}

