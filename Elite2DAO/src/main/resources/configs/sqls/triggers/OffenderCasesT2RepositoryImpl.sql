OFFENDER_CASES_T2_CASE_IDENTIFIER_EXIST{
select
	'X'
from
	offender_case_identifiers
where
	case_id = :caseId
	and identifier_type = coalesce ( :caseInfoPrefix,
	'CASE/INFO#' )
	and identifier_no = :caseInfoNumber
}
