OMSYSJNT_CREATE_TR {
	SELECT TABLE_NAME FROM ALL_TABLES WHERE TABLE_NAME NOT LIKE '%_JN' AND TABLE_NAME NOT LIKE '%TEMP' AND TABLE_NAME NOT LIKE '%TMP' AND TABLE_NAME NOT LIKE 'TEMP%' AND TABLE_NAME NOT LIKE 'TMP%' AND OWNER = 'oms_owner'
}

