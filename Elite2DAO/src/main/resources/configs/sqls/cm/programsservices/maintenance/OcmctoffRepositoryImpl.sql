 
OCMCTOFF_FIND_RGPSSEX {
 	SELECT
  REF_CODE.DESCRIPTION, REF_CODE.CODE
FROM REFERENCE_CODES REF_CODE
WHERE
  DOMAIN = 'PS_SEX' AND
  ( ( ACTIVE_FLAG = 'Y' AND EXPIRED_DATE IS NULL ))
ORDER BY
  LIST_SEQ, DESCRIPTION, CODE

}

