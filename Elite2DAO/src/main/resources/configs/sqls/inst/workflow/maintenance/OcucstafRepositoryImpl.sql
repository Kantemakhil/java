
OCUCSTAF_FIND_RGAGENCYTYPE {
 	SELECT DESCRIPTION AGENCY_TYPE , CODE   FROM REFERENCE_CODES  WHERE DOMAIN = 'AREA_TYPE'    AND ACTIVE_FLAG = 'Y' ORDER BY LIST_SEQ , DESCRIPTION , CODE
}

OCUCSTAF_FIND_RGAREA {
 	SELECT AREAS.DESCRIPTION DESCRIPTION , AREAS.AREA_CODE CODE   FROM AREAS  WHERE AREA_TYPE = :areaType  ORDER BY AREAS.DESCRIPTION
}

OCUCSTAF_FIND_RGSTAFFSTATUS {
 	SELECT DESCRIPTION STAFF_STATUS , CODE   FROM REFERENCE_CODES  WHERE DOMAIN = 'STAFF_SEARCH'    AND ACTIVE_FLAG = 'Y' ORDER BY LIST_SEQ , DESCRIPTION
}

OCUCSTAF_FIND_RGLOCATION {
 	SELECT DISTINCT DESCRIPTION, AGY_LOC_ID CODE   FROM AGENCY_LOCATIONS   WHERE AGENCY_LOCATION_TYPE = :areaType   AND AREA_CODE = :areaCode    AND ACTIVE_FLAG = 'Y'  ORDER BY AGENCY_LOCATIONS.DESCRIPTION
}

OCUCSTAF_FIND_RGROLE {
 	SELECT REFERENCE_CODES.DESCRIPTION ROLE , REFERENCE_CODES.CODE CODE    FROM REFERENCE_CODES  WHERE DOMAIN = 'STAFF_ROLE'    AND ACTIVE_FLAG = 'Y' ORDER BY LIST_SEQ , ROLE
}

OCUCSTAF_FIND_RGPOSITION {
 	SELECT DESCRIPTION POSITION , CODE FROM REFERENCE_CODES WHERE DOMAIN = 'STAFF_POS' AND ACTIVE_FLAG = 'Y' ORDER BY LIST_SEQ , DESCRIPTION
}

OCUCSTAF_FIND_DETAILS {
SELECT DISTINCT
      last_name, first_name,slr.position, 
      slr.role, from_date, to_date, sm.staff_id, slr.SCHEDULE_TYPE, slr.HOURS_PER_WEEK                    
     FROM
      staff_members sm,
      staff_location_roles slr
      where slr.sac_staff_id = sm.staff_id and sm.status='ACTIVE'  order by last_name, first_name
      
}