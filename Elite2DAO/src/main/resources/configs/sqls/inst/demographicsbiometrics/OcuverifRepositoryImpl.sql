OCUVERIF_OFFENDER_SECURITY_LEVEL{
SELECT OFF_ASS.REVIEW_SUP_LEVEL_TYPE FROM OFFENDER_ASSESSMENTS OFF_ASS WHERE OFF_ASS.OFFENDER_BOOK_ID = :offenderBookId 
AND OFF_ASS.ASSESSMENT_SEQ = 
(SELECT MAX(OFF_ASS.ASSESSMENT_SEQ) FROM OFFENDER_ASSESSMENTS OFF_ASS, ASSESSMENTS ASS WHERE OFF_ASS.OFFENDER_BOOK_ID =
:offenderBookId AND OFF_ASS.ASSESSMENT_TYPE_ID = ASS.ASSESSMENT_ID AND ASS.CASELOAD_TYPE =
(SELECT CL.CASELOAD_TYPE FROM STAFF_MEMBERS SM, CASELOADS CL WHERE SM.USER_ID = :createUserId AND SM.WORKING_CASELOAD_ID = CL.CASELOAD_ID)
AND ASS.DETERMINE_SUP_LEVEL_FLAG ='Y' 
AND OFF_ASS.REVIEW_SUP_LEVEL_TYPE IS NOT null)
}
