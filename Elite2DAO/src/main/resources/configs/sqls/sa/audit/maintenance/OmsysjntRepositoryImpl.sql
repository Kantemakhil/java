OMSYSJNT_CREATE_ONE_TR {
	SELECT    COLUMN_NAME, DATA_TYPE, to_char(DATA_LENGTH) DATA_LENGTH, DATA_PRECISION, DATA_SCALE FROM      USER_TAB_COLUMNS WHERE  table_name= :tableName AND     DATA_TYPE IN ('DATE', 'NUMBER', 'CHAR', 'VARCHAR2') ORDER BY  COLUMN_ID
}

