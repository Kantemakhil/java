OIMSATYP_SENTENCEADJUSTMENTS_INSERT_SENTENCE_ADJUSTMENTS {
 insert into SENTENCE_ADJUSTMENTS(SENTENCE_ADJUST_CODE, DESCRIPTION, DEBIT_CREDIT_CODE, USAGE_CODE, CREATE_DATETIME, CREATE_USER_ID, MODIFY_DATETIME, EXPIRY_DATE, ACTIVE_FLAG, SEAL_FLAG) values(:sentenceAdjustCode, :description, :debitCreditCode, :usageCode, CURRENT_TIMESTAMP, :createUserId, NULL, :expiryDate, :activeFlag, :sealFlag) 
}
OIMSATYP_SENTENCEADJUSTMENTS_UPDATE_SENTENCE_ADJUSTMENTS {
update OMS_OWNER.SENTENCE_ADJUSTMENTS set DESCRIPTION =:description, DEBIT_CREDIT_CODE =:debitCreditCode, USAGE_CODE =:usageCode, MODIFY_DATETIME = CURRENT_TIMESTAMP, MODIFY_USER_ID = :modifyUserId, EXPIRY_DATE =:expiryDate, ACTIVE_FLAG =:activeFlag, SEAL_FLAG =:sealFlag where SENTENCE_ADJUST_CODE =:sentenceAdjustCode 
}
OIMSATYP_SENTENCEADJUSTMENTS_DELETE_SENTENCE_ADJUSTMENTS { 
	DELETE FROM SENTENCE_ADJUSTMENTS where SENTENCE_ADJUST_CODE = :sentenceAdjustCode
}
OIMSATYP_CGFKCHK_SENTENCE_ADJUSTMENTS {
