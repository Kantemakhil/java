
OCDLANGS_FIND_RGLANGSKILLS {
 	SELECT
  DESCRIPTION, CODE
FROM REFERENCE_CODES
WHERE
  DOMAIN = 'LANG_SKILLS' AND
  ((ACTIVE_FLAG = 'Y' AND EXPIRED_DATE IS NULL ))
ORDER BY
  REFERENCE_CODES.LIST_SEQ, REFERENCE_CODES.DESCRIPTION

}

