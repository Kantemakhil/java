OCUVERIF_WORK_FLOW_ID {
SELECT WORK_FLOW_ID  FROM   WORK_FLOWS WHERE  OBJECT_CODE = 'ALERT' AND  OBJECT_ID  = :V_BOOK_ID AND   OBJECT_SEQ  = :V_ALERT_SEQ
}

OCUVERIF_GET_WORK_FLOW_SEQ {
SELECT coalesce (MAX (WORK_FLOW_SEQ), 0) + 1 FROM WORK_FLOW_LOGS WHERE WORK_FLOW_ID = :WORK_FLOW_ID
}
