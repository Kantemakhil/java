OCUUPSTA_ADMINISTRATER_USER{
SELECT ROLE_ID FROM STAFF_MEMBER_ROLES WHERE STAFF_ID IN (SELECT STAFF_ID FROM STAFF_MEMBERS WHERE USER_ID=:user) AND STAFF_MEMBER_ROLES.ROLE_CODE =:roleCode
}

