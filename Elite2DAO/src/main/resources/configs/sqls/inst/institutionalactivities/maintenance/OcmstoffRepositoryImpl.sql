OCMSTOFF_PRGPRFGD_FIND_PROGRAM_SERVICES_PROFILES {
 SELECT PROGRAM_ID ,PROGRAM_PROFILE_TYPE ,PROGRAM_PROFILE_CODE,PROGRAM_PROFILE_CODE PROFILE_GENDER_CODE,CREATE_DATETIME ,CREATE_USER_ID ,MODIFY_DATETIME ,MODIFY_USER_ID ,SEAL_FLAG   FROM PROGRAM_SERVICES_PROFILES   where PROGRAM_ID=:programId AND PROGRAM_PROFILE_TYPE =:programProfileType
order by PROGRAM_PROFILE_CODE in (SELECT  PROGRAM_PROFILE_CODE FROM REFERENCE_CODES WHERE  domain = 'PS_SEX') 
}
OCMSTOFF_PRGPRFGD_INSERT_PROGRAM_SERVICES_PROFILES {
insert into PROGRAM_SERVICES_PROFILES(PROGRAM_ID , PROGRAM_PROFILE_TYPE , PROGRAM_PROFILE_CODE , CREATE_DATETIME , CREATE_USER_ID , MODIFY_DATETIME , SEAL_FLAG ) values(:programId , :programProfileType , :programProfileCode , CURRENT_TIMESTAMP , :createUserId , null , :sealFlag )
}

OCMSTOFF_PRGPRFGD_UPDATE_PROGRAM_SERVICES_PROFILES {
update PROGRAM_SERVICES_PROFILES set PROGRAM_ID = :programId , PROGRAM_PROFILE_TYPE = :programProfileType , PROGRAM_PROFILE_CODE = :programProfileCode , MODIFY_DATETIME = CURRENT_TIMESTAMP , MODIFY_USER_ID = :modifyUserId , SEAL_FLAG = :sealFlag 
}

OCMSTOFF_PRGPRFGD_DELETE_PROGRAM_SERVICES_PROFILES { 
	DELETE FROM PROGRAM_SERVICES_PROFILES where PROGRAM_ID  = :programId AND PROGRAM_PROFILE_TYPE  = :programProfileType AND PROGRAM_PROFILE_CODE  = :programProfileCode
}
