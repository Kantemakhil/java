OTDOPCTA_OFFENDER_TRUST_ACCOUNT_TXN_TYPE {
  SELECT TXN_TYPE    FROM TRANSACTION_OPERATIONS WHERE MODULE_NAME = :MODULENAME AND CASELOAD_ID = :CASELOADID
}

OTDOPCTA_ACCOUNT_CODES_TYPE {
SELECT sub_account_type,txn_posting_type
  FROM   account_codes
      WHERE  account_code = (SELECT cr_account_code
                               FROM transaction_operations
                              WHERE module_name =:MODULENAME
                                AND txn_type = :TXNTYPE
				AND caseload_id = :CASELOADID) 
      AND CASELOAD_TYPE = :CASELOADTYPE --('Error: unable to get the offender sub account type to credit.');
  }
  
OTDOPCTA_TXN_ID_SEQUENCE {
 SELECT NEXTVAL('TXN_ID') next_seq from dual
}

