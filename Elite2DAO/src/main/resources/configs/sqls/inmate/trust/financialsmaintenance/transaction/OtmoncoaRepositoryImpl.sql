}

OTMONCOA_CSLDAP_DELETE_CASELOAD_ACCOUNT_PERIODS { 
	DELETE FROM CASELOAD_ACCOUNT_PERIODS  WHERE ACCOUNT_PERIOD_ID = :accountPeriodId
