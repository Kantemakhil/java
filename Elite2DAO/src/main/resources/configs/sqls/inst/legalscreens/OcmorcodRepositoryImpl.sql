
OCMORCOD_FIND_RGRESCODNBTDESCRIPTION {
 	SELECT REF_CODE.DESCRIPTION NBT_DESCRIPTION , REF_CODE.CODE DISPOSITION_CODE FROM REFERENCE_CODES REF_CODE  WHERE DOMAIN = 'OFF_RESULT' AND ACTIVE_FLAG = 'Y'
}

OCMORCOD_FIND_RGRESCODCHARGESTATUS {
 	SELECT DESCRIPTION , CODE   FROM REFERENCE_CODES  WHERE DOMAIN = 'CHARGE_STS'    AND ACTIVE_FLAG = 'Y' 
}

OCMORCOD_RESCOD_FIND_OFFENCE_RESULT_CODES {
 	SELECT RESULT_CODE ,DESCRIPTION ,DISPOSITION_CODE ,CHARGE_STATUS ,ACTIVE_FLAG ,EXPIRY_DATE ,CREATE_USER_ID ,CREATE_DATETIME ,MODIFY_USER_ID ,MODIFY_DATETIME ,CONVICTION_FLAG ,LIST_SEQ ,SEAL_FLAG, ROW_ID  FROM OFFENCE_RESULT_CODES
}
OCMORCOD_RESCOD_INSERT_OFFENCE_RESULT_CODES {
 insert into OFFENCE_RESULT_CODES(RESULT_CODE , DESCRIPTION , DISPOSITION_CODE , CHARGE_STATUS , ACTIVE_FLAG , EXPIRY_DATE , CREATE_USER_ID , CREATE_DATETIME , MODIFY_DATETIME , CONVICTION_FLAG , LIST_SEQ , SEAL_FLAG ) values(:resultCode , :description , :dispositionCode , :chargeStatus , :activeFlag , :expiryDate , :createUserId , CURRENT_TIMESTAMP , NULL , :convictionFlag , :listSeq , :sealFlag ) 
}

OCMORCOD_RESCOD_UPDATE_OFFENCE_RESULT_CODES {
 update OFFENCE_RESULT_CODES set RESULT_CODE = :resultCode , DESCRIPTION = :description , DISPOSITION_CODE = :dispositionCode , CHARGE_STATUS = :chargeStatus , ACTIVE_FLAG = :activeFlag , EXPIRY_DATE = :expiryDate , MODIFY_USER_ID = :modifyUserId , MODIFY_DATETIME = CURRENT_TIMESTAMP , CONVICTION_FLAG = :convictionFlag , LIST_SEQ = :listSeq , SEAL_FLAG = :sealFlag where ROW_ID = :rowId
}


OCMORCOD_OCMORCOD_WHENVALIDATEITEM_ {
	SELECT COUNT(*) FROM OFFENCE_RESULT_CODES WHERE RESULT_CODE =LV_RESULT_CODE
}

OCMORCOD_CREATE_FORM_GLOBALS {
	SELECT DESCRIPTION INTO V_FORM_DESC FROM OMS_MODULES WHERE MODULE_NAME = V_FORM_NAME
}
OCMORCOD_GET_CHARGE_STATUS_BASED_ON_RESULT_CODE {
select CHARGE_STATUS from  OFFENCE_RESULT_CODES where result_code=:chargeCode
