	insert into OFF_FEE_ACCOUNT_OVERRIDES(ADDED_BY_STAFF_ID,
ADDED_DATE,
COMMENT_TEXT,
OFFENDER_FEE_ID,
OFF_FEE_OVERRIDE_ID,
OVERRIDE_AMOUNT,
OVERRIDE_END_DATE,
OVERRIDE_START_DATE,
OVERRIDE_TYPE,
PRIORITY_INDICATOR,
CREATE_DATETIME,
CREATE_USER_ID,
SEAL_FLAG) values(:addedByStaffId,:addedDate,:commentText,:offenderFeeId,:offFeeOverrideId,:overrideAmount,:overrideEndDate,:overrideStartDate,:overrideType,:priorityIndicator,current_timestamp,:createUserId,:sealFlag)
}

