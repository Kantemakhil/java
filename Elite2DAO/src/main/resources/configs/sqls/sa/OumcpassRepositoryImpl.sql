
OUMCPASS_STAFF_FIND_STAFF_MEMBERS {
 	SELECT  * FROM STAFF_MEMBERS  WHERE STAFF_MEMBERS.USER_ID = USER
}
OUMCPASS_STAFF_UPDATE_STAFF_MEMBERS {
	 update STAFF_MEMBERS set FORCE_PASSWORD_CHANGE_FLAG = 'N', LAST_PASSWORD_CHANGE_DATE = CURRENT_TIMESTAMP, modify_datetime = current_timestamp, modify_user_id =:modifyUserId where USER_ID = :L_USER_NAME
}

