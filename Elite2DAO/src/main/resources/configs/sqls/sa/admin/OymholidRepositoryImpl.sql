OYMHOLID_CSLDGHC_FIND_CASELOAD_GRP_HOL_COMPENS {
select
CASELOAD_ID,
COMPENSATION_CODE,
CREATE_DATETIME,
CREATE_USER_ID,
HOLIDAY_EVENT_ID,
MODIFY_DATETIME,
MODIFY_USER_ID,
SEAL_FLAG,
WORK_GROUP_ID,
ROW_ID
from
CASELOAD_GRP_HOL_COMPENS
where
CASELOAD_ID = :caseloadId
and HOLIDAY_EVENT_ID = :holidayEventId
order by row_id

}
