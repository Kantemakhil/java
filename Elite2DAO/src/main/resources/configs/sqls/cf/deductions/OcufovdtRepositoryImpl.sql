	UPDATE OFF_FEE_ACCOUNT_OVERRIDES SET ADDED_BY_STAFF_ID=:addedByStaffId,
ADDED_DATE=:addedDate,
COMMENT_TEXT=:commentText,
OVERRIDE_AMOUNT=:overrideAmount,
OVERRIDE_END_DATE=:overrideEndDate,
OVERRIDE_START_DATE=:overrideStartDate,
OVERRIDE_TYPE=:overrideType,
PRIORITY_INDICATOR=:priorityIndicator,
SEAL_FLAG=:sealFlag, 
MODIFY_DATETIME=current_timestamp,MODIFY_USER_ID=:modifyUserId where OFF_FEE_OVERRIDE_ID=:offFeeOverrideId
}

